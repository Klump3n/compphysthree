import numpy as np

def plot_data(data, title=None, save_name=None):
    if save_name is not None:
        import matplotlib as mpl
        mpl.use('Agg')
    import matplotlib.pyplot as plt

    if title:
        print(title)

    data_len = len(data)
    max_len = int(data_len / 2)

    boltz_exp = []
    mag = []

    for it in range(max_len):
        boltz_exp.append(data[2*it+0])
        mag.append(data[2*it+1])

    fig1 = plt.figure()
    plt.plot(boltz_exp, label='Boltzmann Exponent')
    plt.plot(mag, label='Magnetisierung')
    plt.yscale('log')
    plt.xlabel(r'Iterationsschritt $i$')
    if title:
        plt.title('{}'.format(title))
    plt.grid()
    plt.tight_layout()

    if save_name is None:
        plt.show()
    else:
        plt.savefig('../figures/{}.pdf'.format(save_name))

    fig2 = plt.figure()
    plt.plot(boltz_exp, label='Boltzmann Exponent')
    plt.yscale('log')
    plt.xlabel(r'Iterationsschritt $i$')
    if title:
        plt.title('Boltzmann Exponent fuer {}'.format(title))
    plt.grid()
    plt.tight_layout()

    if save_name is None:
        plt.show()
    else:
        plt.savefig('../figures/{}_boltzmann.pdf'.format(save_name))

    fig3 = plt.figure()
